var day = 2;

switch (day) {
  case 1:
    console.log("Monday");
  case 2:
    console.log("Tuesday");
  case 3:
    console.log("Wed");
  case 4:
    console.log("Thrus");
  case 5:
    console.log("Fri");
  case 6:
    console.log("Sat");
  case 7:
    console.log("Sun");
    break;
  default:
    console.log("Invalid Input");

}